"""Ce module contient une implémentation de fonctions mathématiques simples."""
class simple_math:
    """Classe pour des opérations mathématiques simples."""
    @staticmethod
    def addition(a, b):
        """Retourne la somme de deux nombres."""
        return a + b
    
    @staticmethod
    def soustraction(a, b):
        """Retourne le reste de la soustraction."""
        return a - b